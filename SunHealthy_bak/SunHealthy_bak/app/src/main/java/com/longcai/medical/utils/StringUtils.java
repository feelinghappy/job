    public static String getResPath(Context context, int resId, String name) {
        File file;
        String path = null;
        if (UtilSDCard.isSDCardEnable()) {
            file = new File(Environment.getExternalStorageDirectory() + "/medical/");
            if (file.exists()) {
                StringUtils.recursionDeleteFile(file);
            }
        } else {
            file = new File(context.getApplicationInfo().dataDir + "/medical/");
        }
        Bitmap bitmap = BitmapFactory.decodeResource(context.getResources(), resId);
        FileOutputStream fos = null;
        try {
            path = file.getCanonicalPath() + name;
            fos = new FileOutputStream(path);
            bitmap.compress(Bitmap.CompressFormat.JPEG, 100, fos);
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            if (fos != null) {
                try {
                    fos.flush();
                    fos.close();
                } catch (IOException e) {
                }
            }
        }
        return path;
    }

